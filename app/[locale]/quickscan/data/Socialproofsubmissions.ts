// Helper function to get a random submission
export const getRandomSubmission = (): Submission => {
  return recentSubmissions[Math.floor(Math.random() * recentSubmissions.length)];
};

// Helper function to shuffle submissions
export const shuffleSubmissions = (): Submission[] => {
  const shuffled = [...recentSubmissions];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};